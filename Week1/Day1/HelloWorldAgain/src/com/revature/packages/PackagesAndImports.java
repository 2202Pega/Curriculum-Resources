package com.revature.packages;

public class PackagesAndImports {
	
	/*
	 * Packages, Repositories and Folders! 
	 * 		These are essentially the same thing, but used in different
	 * 		contexts!
	 * 
	 * 		In the context of Java Projects, we call folders packages. 
	 * 
	 * 		com.revature == com/revature
	 * 		com.revature IS NOT A FOLDER called com.revature
	 * 
	 * 		Why do we use packages in Java projects?
	 * 			1) Searching and locating classes!
	 * 			2) Provide controlled access
	 * 			3) Prevents Naming Conflicts
	 * 
	 * 		naming convention! 
	 * 		in Java, when using packages, we use lower case!
	 * 
	 * 		fruits and vegetables (INCORRECT)
	 * 		fruits_and_vegetables (INCORRECT)
	 * 		fruitsandvegetables (CORRECT)
	 * 		
	 */

}
